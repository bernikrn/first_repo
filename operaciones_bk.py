#ingesos
input_1 = int(input("ingrese un numero: "))
input_2 = int(input("ingrese otro numero: "))

#resulados
print(input_1, " + ", input_2, " =", input_1+input_2)
print(input_1, " - ", input_2, " =", input_1-input_2)
print(input_1, " / ", input_2, " =", input_1/input_2)
print(input_1, " * ", input_2, " =", input_1*input_2)
